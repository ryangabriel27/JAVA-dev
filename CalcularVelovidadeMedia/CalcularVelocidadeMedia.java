package CalcularVelovidadeMedia;
public class CalcularVelocidadeMedia {

    public static void main(String[] args) {
        System.out.println("===CALCULAR A VELOCIDADE MÉDIA===");
        double distanciaPercorrida = 150; 
        System.out.println("a Distância é "+distanciaPercorrida+" KM");
        double tempoGasto = 2;
        System.out.println("O tempo gasto foi "+tempoGasto+" Hrs");
        double velocidadeMedia = distanciaPercorrida / tempoGasto;
        System.out.println("A velocidade média é "+velocidadeMedia+" Km por hrs");
    

    }
} 
    

